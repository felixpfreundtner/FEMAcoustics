'1',...
'0')
pdesetbd(3,...
'dir',...
1,...
'1',...
'0')
pdesetbd(2,...
'dir',...
1,...
'1',...
'0')
pdesetbd(1,...
'dir',...
1,...
'1',...
'0')

% Mesh generation:
setappdata(pde_fig,'trisize',0.0040000000000000001);
setappdata(pde_fig,'Hgrad',1.3);
setappdata(pde_fig,'refinemethod','longest');
setappdata(pde_fig,'jiggle',char('off','on',''));
setappdata(pde_fig,'MesherVersion','preR2013a');
pdetool('initmesh')

% PDE coefficients:
pdeseteq(1,...
'1.0!1.0!1.0',...
'0.0!0.0!0.0',...
'10.0!10.0!10.0',...
'1.0!1.0!1.0',...
'0:10',...
'0.0',...
'0.0',...
'[0 100]')
