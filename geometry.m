% Geometry description:
pdeellip(-0.02,7.0169932518057698e-05,0.01,0.01,...
0,'E1');
pdeellip(0.02,0,0.01,0.01,...
0,'E2');
pdeellip(0,0,0.10000000000000001,0.10000000000000001,...
0,'E3');
pderect([-0.20000000000000001 0.20000000000000001 0 -0.20000000000000001],'R1');
set(findobj(get(pde_fig,'Children'),'Tag','PDEEval'),'String','(E1+E2+E3)-R1')

% Boundary conditions:
pdetool('changemode',0)
pdesetbd(12,...
'dir',...
1,...
'1',...
'0')
pdesetbd(11,...
'dir',...
1,...
'1',...
'0')
pdesetbd(5,...
'dir',...
1,...
'1',...
'0')
pdesetbd(4,...
'dir',...
